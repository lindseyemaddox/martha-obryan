		<h1>Head of K-8 Education</h1>

		<h2>6/23/2014</h5>
	
		<p>Martha O'Bryan Center is a creative activist ministry serving over 9,000 clients in Nashville each year through early learning education, youth development, Work Ready job placement, family resource center community/social services and East End Prep K-5 & Explore Community charter schools.</p>

		<p>Mission Statement On a foundation of Christian faith, Martha O'Bryan Center empowers children, youth, and adults in poverty to transform their lives through work, education, employment, and fellowship.</p>

		<p>The Head of K-8 Education provides leadership and directs the K-8 Education team to develop educational programs and curriculum and deliver out of school time activities to children in Nashville’s public schools. The Head directs academic support, enrichment activities (including sports and the humanities), social and emotional learning, parenting support and programs, community service and college preparation, curriculum and instruction strategies development, strategic plan implementation, and staff management and development across a variety of sites, leaderships roles, and youth development and education professionals.  In collaboration with the other department heads, the Head of K-8 Education supports the successful transition of students from elementary school to middle school, high school, and post-secondary pursuits.  The Head assumes a leadership role in curriculum and instruction planning and integration with school based partners, and state and federal standards, as well as leads assessment of current instructional practices and curriculum and makes recommendations for continuous improvement in K-8 programs.</p>

		<h3>Essential Duties and Responsibilities</h3>

		<ul>

    		<li>Leads the development, implementation and administration of a progressive K-8 out of school program focused on academic achievement, social and emotional development and parent engagement.</li>

    		<li>Manages and develop multi-faceted youth programming across sites and schools with unique needs, including curriculum and instruction strategies development, strategic plan implementation, staff management and development, site management, master schedule development, contract management and compliance and budget management.</li>

    		<li>Implements the Martha O’Bryan Center Strategic plan by developing an annual K-8 program plan. Works in partnership with principals and teachers at school based sites to formulate meaningful youth learning goals, develop annual work plans, and coordinate curricula, academic and enrichment activities, professional development, transportation planning and space use.</li>

    		<li>Assumes a leadership role in curriculum and instruction planning and integration with school based partners, and state and federal standards. Leads assessment of current instructional practices and curriculum and makes recommendations for continuous improvement in K-8 programs.</li>

    		<li>Collaborates with the other department heads to ensure a consistent PreK-12 flow of educational and enrichment activities. Develops and manages progressive parent engagement and support program based on best practice, looking beyond supporting children in school and including building social and economic capital for parents. Includes home visits and coordination of IEPs.</li>

    		<li>Assists with collection and analysis of data for organizational reporting, contract compliance and to improve programs and academic achievement. Manages process of pre and post assessments for students and programs. Works closely with Director of Research & Evaluation to develop evidence base and ensure data driven programming.</li>

    		<li>Manages a team, including site coordinators, extended learning coordinators, academic coaches and tutors to deliver the K-8 program across multiple sites. Manages professional development and team building activities for staff, in coordination with schools where possible. Works with HR to recruit, select and hire program staff. Conducts regular site visits and audits.</li>

	    	<li>Manages all aspects of program funding and contracts. Includes researching funding opportunities, proposal writing in partnership with the Chief Program Officer and MOBC grant writers. Assumes responsibility for all contract compliance, management of audits.</li>

		</ul>

