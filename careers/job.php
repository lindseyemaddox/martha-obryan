		<p>The requirements listed below are representative of the knowledge, skills, and/or abilities required.</p>

		<h4>Education and Experience:</h4>

		<ul>

			<li>Masters degree in Education or related field strongly preferred.</li>
    
    		<li>Demonstrable experience working in youth programming in vulnerable communities.</li>
    		
    		<li>Supervisory and program leadership experience in an educational or social service organization.</li>
    		
    		<li>Grant and budget management, including reporting and compliance for government grants.</li>

		</ul>

		<p>The above qualifications express the minimum standards of education and/or experience for this position. Other combinations of education and experience, if evaluated as equivalent, may be taken into consideration.</p>

