			<p>The Martha O’Bryan Center enables vulnerable families living in Nashville’s most distressed neighborhood overcome the many obstacles limiting them from living healthy, full lives. Experience shows that communities are healthiest when parents are working and children are succeeding in school. To help achieve these aims, we provide a highway of integrated services for the entire family that engages schools and community partners along the way.</p>
 
			<p>People enter our highway at varying life stages, some through their parents even before they are born and others as adults. For a child living in extreme poverty to gain a high quality education and become a productive adult, he or she needs ongoing, expertly-coordinated opportunities and attention to a varied set of complex, inter-related challenges. Similarly, adults may need help in areas such as GED classes and tutoring, job readiness, parenting education and various types of counseling services. Finally, at times, people of all ages may need help with necessities as basic as food. As fuel for the Martha O'Bryan Center Highway, we provide all of these services and more in an integrated, coordinated manner.</p>
 
